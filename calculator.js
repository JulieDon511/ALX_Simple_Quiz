//addition
function add(number1, number2) {
    return number1 + number2;
}
//subtraction
function subtract(number1, number2){
    return number2 - number1;
}

// multiply
function subtract(number1, number2){
    return number1 *number2;
}

//divide
function subtract(number1, number2){
    return number2 / number1;
}